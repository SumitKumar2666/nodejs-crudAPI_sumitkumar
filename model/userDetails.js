
const { DataTypes } = require('sequelize');
const db = require('../config/postgres');

const UserDetails = db.define('UserDetails', {
  name: {
    type: DataTypes.STRING,
    allowNull: false,
  },
  email: {
    type: DataTypes.STRING,
    allowNull: false,
    unique: true,
  },
  password: {
    type: DataTypes.STRING,
    allowNull: false,
  },
});

module.exports = UserDetails;